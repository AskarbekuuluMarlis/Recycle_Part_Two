package com.example.recycleview_parttwo.continent;

public class Continent {
    private String name;
    private String img;

    public Continent(String name, String img) {
        this.name = name;
        this.img = img;

    }

    public String getName() {
        return name;
    }

    public String getImg() {
        return img;
    }
}
